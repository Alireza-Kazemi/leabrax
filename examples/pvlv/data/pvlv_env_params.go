// Trial : epoch-level state
// aka Trial
type Trial struct {
	TrialGpName         string       `desc:"name"`
	PercentOfTotal      float64      `desc:"Percent of all trials for this type"`
	ValenceContext      pvlv.Valence `desc:"Positive or negative reward valence"`
	USProb              float64      `desc:"Probability of US"`
	FixedProb           bool         `desc:"Fixed probability?"`
	MixedUS             bool         `desc:"Mixed US set?"`
	USMagnitude         float64      `desc:"US magnitude"`
	AlphTicksPerTrialGp int          `desc:"Number of ticks for a trial"`
	CS                  string       `desc:"Conditioned stimulus"`
	CSTimeStart         Tick         `desc:"Tick of CS start"`
	CSTimeEnd           Tick         `desc:"Tick of CS end"`
	CS2TimeStart        Tick         `desc:"Tick of CS2 start"`
	CS2TimeEnd          Tick         `desc:"Tick of CS2 end"`
	USType              US           `desc:"Unconditioned stimulus"`
	USTimeStart         Tick         `desc:"Tick for start of US presentation"`
	USTimeEnd           Tick         `desc:"Tick for end of US presentation"`
	Context             string       `desc:"Context"`
	Location            string       `desc:"Location"`
}

type TrialList []*Trial

type EnvParamsMap map[string]*TrialList

type EnvParams struct {
	//RecordList
	Trials *TrialList
	Index  []int
	INext  int
	NRead  int
	Order  rlist.DataLoopOrder
}

func IntSequence(begin, end, step int) (sequence []int) {
	if step == 0 {
		panic(errors.New("step must not be zero"))
	}
	count := 0
	if (end > begin && step > 0) || (end < begin && step < 0) {
		count = (end-step-begin)/step + 1
	}

	sequence = make([]int, count)
	for i := 0; i < count; i, begin = i+1, begin+step {
		sequence[i] = begin
	}
	return
}

func (ep *EnvParams) New(tgs *TrialList) *EnvParams {
	ep.Trials = tgs
	ep.Index = IntSequence(0, len(*tgs), 1)
	ep.INext = -1
	ep.NRead = 0
	return ep
}
func (ep *EnvParams) Init() *EnvParams {
	ep.NRead = 0
	ep.INext = -1
	ep.Index = []int{}
	ep.Trials = &TrialList{}
	return ep
}
func (ep *EnvParams) Permute() {
	ep.Index = rand.Perm(len(*ep.Trials))
}
func (ep *EnvParams) Sequential() {
	ep.Index = IntSequence(0, len(*ep.Trials), 1)
}
func (ep *EnvParams) SetIndex(ix []int) error {
	if len(ix) > len(*ep.Trials) {
		return errors.New("supplied index is longer than data")
	}
	ep.Index = ix
	ep.Reset()
	return nil
}
func (ep *EnvParams) SetOrder(order rlist.DataLoopOrder) {
	ep.Order = order
	if order == rlist.SEQUENTIAL {
		ep.Sequential()
	} else if order == rlist.PERMUTED {
		ep.Permute()
	}
	ep.Reset()
}
func (ep *EnvParams) GetIndex() []int {
	return ep.Index
}
func (ep *EnvParams) WriteNext(tg *Trial) {
	*ep.Trials = append(*ep.Trials, tg)
	ep.Index = append(ep.Index, len(*ep.Trials)-1)
}
func (ep *EnvParams) ReadNext() *Trial {
	tgs := *ep.Trials
	ep.NRead++
	if ep.Order == rlist.RANDOM {
		return tgs[rand.Intn(len(*ep.Trials))]
	} else {
		ep.INext++
		return tgs[ep.Index[ep.INext]]
	}
}
func (ep *EnvParams) Peek() *Trial {
	return (*ep.Trials)[ep.INext]
}
func (ep *EnvParams) AtEnd() bool {
	return ep.NRead >= len(*ep.Trials)
}
func (ep *EnvParams) Reset() {
	ep.NRead = 0
	ep.INext = -1
}
func (ep *EnvParams) SetPos(i int) {
	ep.INext = i - 1
}
func (ep *EnvParams) Length() int {
	return len(*ep.Trials)
}
func (ep *EnvParams) Cur() int {
	return ep.INext + 1
}

